public class PishPardakhtReport {
    private String name;
    private String shenasame;
    private String kodeMelli;
    private String nameBimeshode;
    private String haghBime;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getShenasame() {
        return shenasame;
    }

    public void setShenasame(String shenasame) {
        this.shenasame = shenasame;
    }

    public String getKodeMelli() {
        return kodeMelli;
    }

    public void setKodeMelli(String kodeMelli) {
        this.kodeMelli = kodeMelli;
    }

    public String getNameBimeshode() {
        return nameBimeshode;
    }

    public void setNameBimeshode(String nameBimeshode) {
        this.nameBimeshode = nameBimeshode;
    }

    public String getHaghBime() {
        return haghBime;
    }

    public void setHaghBime(String haghBime) {
        this.haghBime = haghBime;
    }
}
